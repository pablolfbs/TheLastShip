package model;

public class Usuario {
	
	private Integer usuario_id;
	private String usuario_cpf;
	private String usuario_nome;
	private String usuario_sobrenome;
	private String usuario_funcao;
	private String usuario_login;
	private String usuario_senha;
	private Integer usuario_status;
	
	public Integer getUsuario_id() {
		return usuario_id;
	}
	public void setUsuario_id(Integer usuario_id) {
		this.usuario_id = usuario_id;
	}
	public String getUsuario_cpf() {
		return usuario_cpf;
	}
	public void setUsuario_cpf(String usuario_cpf) {
		this.usuario_cpf = usuario_cpf;
	}
	public String getUsuario_nome() {
		return usuario_nome;
	}
	public void setUsuario_nome(String usuario_nome) {
		this.usuario_nome = usuario_nome;
	}
	public String getUsuario_sobrenome() {
		return usuario_sobrenome;
	}
	public void setUsuario_sobrenome(String usuario_sobrenome) {
		this.usuario_sobrenome = usuario_sobrenome;
	}
	public String getUsuario_funcao() {
		return usuario_funcao;
	}
	public void setUsuario_funcao(String usuario_funcao) {
		this.usuario_funcao = usuario_funcao;
	}
	public String getUsuario_login() {
		return usuario_login;
	}
	public void setUsuario_login(String usuario_login) {
		this.usuario_login = usuario_login;
	}
	public String getUsuario_senha() {
		return usuario_senha;
	}
	public void setUsuario_senha(String usuario_senha) {
		this.usuario_senha = usuario_senha;
	}
	public Integer getUsuario_status() {
		return usuario_status;
	}
	public void setUsuario_status(Integer usuario_status) {
		this.usuario_status = usuario_status;
	}
	
	

}
